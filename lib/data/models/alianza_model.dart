import 'package:paap/domain/entities/alianza_entity.dart';

class AlianzaModel extends AlianzaEntity {
  AlianzaModel({
    String? alianzaId,
    String? perfilPreInversionId,
    String? convocatoriaId,
    String? nombre,
    String? abreviatura,
    String? nit,
    String? municipioId,
    String? direccion,
    String? contacto,
    String? telefonoFijo,
    String? telefonoMovil,
    String? correo,
    String? tipoProyectoId,
    String? productoId,
    String? productoAsociadoId,
    String? valorTotalProyecto,
    String? incentivoModular,
    String? legalizado,
  }) : super(
            alianzaId: alianzaId,
            perfilPreInversionId: perfilPreInversionId,
            convocatoriaId: convocatoriaId,
            nombre: nombre ?? '',
            abreviatura: abreviatura ?? '',
            nit: nit ?? '',
            municipioId: municipioId,
            direccion: direccion ?? '',
            contacto: contacto ?? '',
            telefonoFijo: telefonoFijo ?? '',
            telefonoMovil: telefonoMovil ?? '',
            correo: correo ?? '',
            tipoProyectoId: tipoProyectoId,
            productoId: productoId,
            productoAsociadoId: productoAsociadoId,
            valorTotalProyecto: valorTotalProyecto ?? '',
            incentivoModular: incentivoModular ?? '',
            legalizado: legalizado ?? '');

  factory AlianzaModel.fromJson(Map<String, dynamic> json) => AlianzaModel(
        alianzaId: json["AlianzaId"],
        perfilPreInversionId: json["PerfilPreInversionId"],
        convocatoriaId: json["ConvocatoriaId"],
        nombre: json["Nombre"],
        abreviatura: json["Abreviatura"],
        nit: json["NIT"],
        municipioId: json["MunicipioId"],
        direccion: json["Direccion"],
        contacto: json["Contacto"],
        telefonoFijo: json["TelefonoFijo"],
        telefonoMovil: json["TelefonoMovil"],
        correo: json["Correo"],
        tipoProyectoId: json["TipoProyectoId"],
        productoId: json["ProductoId"],
        productoAsociadoId: json["ProductoAsociadoId"],
        valorTotalProyecto: json["ValorTotalProyecto"],
        incentivoModular: json["IncentivoModular"],
        legalizado: json["Legalizado"],
      );
}
